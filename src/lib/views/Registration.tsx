import { TeacherProfile, SchoolLevel, AppLanguage } from '../../types';
import {
  User, School, BookOpen, Check, Loader2, Mail, Key, Sun, Moon,
  LogIn, ArrowRight, MapPin, ChevronLeft, ChevronRight,
  Award, Eye, EyeOff, AlertCircle, RotateCcw, Fingerprint, RefreshCcw
} from 'lucide-react';
import { useAuth } from '../../contexts/AuthContext';
import { TamkeenLogo } from '../../components/ui/TamkeenLogo';

interface Props {
  TamkeenLogo: React.FC<any>;
  useBackend?: boolean;
  onRegister?: (profile: TeacherProfile) => void;
  darkMode: boolean;
  setDarkMode: (val: boolean) => void;
  initialUser?: any;
  lang?: AppLanguage;
  setLang?: (lang: AppLanguage) => void;
  onBack?: () => void;
}

